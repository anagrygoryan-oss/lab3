#include <stdio.h>

int main() {
    int x, z;
    float y;

    printf("Введіть ціле число x: ");
    scanf("%d", &x);

    printf("Введіть ціле число z: ");
    scanf("%d", &z);
    
    if (x == 5) {
        y = 3 + (float)x * z;
        printf("Результат y = %.2f\n", y);
    } 
    else if (x == -5) {
        // Обов'язкова перевірка знаменника!
        if (7 - z == 0) {
            printf("Повідомлення: Обчислити неможливо (ділення на нуль).\n");
        } else {
            // Додаємо дужки для чисельника
            y = (float)(x + z) / (7 - z);
            printf("Результат y = %.2f\n", y);
        }
    } 
    else if (x == 0) {
        y = (float)x;
        printf("Результат y = %.2f\n", y);
    } 
    else {
        // Цей блок спрацює, якщо жодна з умов вище не підійшла
        printf("Повідомлення: Обчислити неможливо, x не дорівнює 5, -5 або 0.\n");
    }

    return 0;
}